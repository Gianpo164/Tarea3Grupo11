package logica;

/**
 * Moneda con valor 1000
 */
public class Moneda1000 extends Moneda {
    /**
     * Genera una moneda
     */
    public Moneda1000() {
        super();
    }

    /**
     * Retorna el valor de la moneda el cual es 1000
     * @return El valor de la moneda el cual es 100
     */
    public int getValor() {
        return 1000;
    }
}
